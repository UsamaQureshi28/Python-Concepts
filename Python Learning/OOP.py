class Employee:
    def __init__(self,name,salary):    #Constructor
        self.name=name
        self.salary=salary
student1=Employee("Usama",1500000)
print(student1.name)
print(student1.salary)
student2=Employee("Hashim",1000000)
print(student2.name)
print(student2.salary)

